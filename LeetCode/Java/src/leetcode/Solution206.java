package leetcode;
import leetcode.common.ListNode;

/**
 * Reverse Linked List.
 * Reverse a singly linked list.
 * @author Johnny
 */
public class Solution206 {
    public ListNode reverseList(ListNode head) {
        ListNode prev = null;
        while (head != null) {
            ListNode next = head.next;
            head.next = prev;
            prev = head;
            head = next;
        }
        return prev;
    }
}
